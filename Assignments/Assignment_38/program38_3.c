#include<stdio.h>
#include <ctype.h>

////////////////////////////////////////////////////////////////////////
//
//  Function Name : StrCpyX
//  Description : small character into capital case     
// 
//  Input  :   char*
//  Output :   void      
//  Author :   Pallavi Kishanrao Hivrale
//  Date   :   30/11/25
//
////////////////////////////////////////////////////////////////////////
void StrCpyCap(char *src, char *dest)
{
    while(*src != '\0')
    {
        *dest = toupper(*src);
        dest++;
        src++;
    }
    *dest = '\0';

}
////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[30] = "Marvellous Python 2";
    char brr[30];           

    StrCpyCap(arr, brr);

    printf("%s",brr);
    
    return 0;
}
