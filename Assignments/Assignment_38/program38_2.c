void StrCpyX(char *src, char *dest)
{
    while(*src != '\0')
    {
        if (*src != ' ')
        {
            *dest = *src;
            dest++;
        }
        src++;
    }
    *dest = '\0';

}
////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[30] = "Marvellous Python";
    char brr[30];           

    StrCpyX(arr, brr);

    printf("%s",brr);
    
    return 0;
}
