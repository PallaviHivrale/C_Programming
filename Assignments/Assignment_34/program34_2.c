char Display(char ch)
{
   if(ch >= 'A' && (ch <= 'Z'))
   {
      ch = ch+32;
   }
   else  if(ch>= 'a' && (ch<='z'))
   {
      ch = ch-32;
   }
    printf("updated character is : %c\n",ch);
   
}

