//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////

int main()
{
    char arr[20];
    
    printf("Enter string : ");
    scanf("%[^'\n]s",arr);

    struprx(arr);

    printf("updated string is %s : ",arr);

    return 0;

}
