int CountSmall(char *str)
{
   int iCount = 0;

   while(*str != '\0')
   {
      if((*str >= 'a') && (*str <= 'z'))
      {
         iCount++;
      }
      str++;
   }
   return iCount;
}

////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////

int main()
{
   char arr[20];
   int iRet = 0;

   printf("Enter string: ");
   scanf("%[^'\n']s", arr);

   iRet = CountSmall(arr);

   printf("%d",iRet);

   return 0;
}

