void Reverse(char *str)
{
   int iCnt =0;

   while(str[iCnt] != '\0')
   {
      iCnt++;
   }
   iCnt--;

   while(iCnt>=0)
   {
      printf("%c",str[iCnt]);
      iCnt--;
   }
  
}

////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////

int main()
{
   char arr[20];

   printf("Enter string: ");
   scanf("%[^'\n']s", arr);

   Reverse(arr);

   return 0;
}

