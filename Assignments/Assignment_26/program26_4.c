void Pattern(int iNo)
{
    int iCnt = 0;

    for(iCnt=1; iCnt<= iNo; iCnt++)
    { 
        printf("#\t%d\t*\t",iCnt);   
    }
    printf("\n");
}
 
////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    printf("Enter number of elements:");
    scanf("%d",&iValue);

    Pattern(iValue);

    return 0;
}

